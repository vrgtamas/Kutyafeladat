    private void btnBetoltesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBetoltesActionPerformed
        try {
           FileReader fajl = new FileReader ("kutyak.txt");
           BufferedReader br = new BufferedReader(fajl);
           String sor = "";
            while ((sor = br.readLine()) != null) {                
                KutyaClass k = new KutyaClass(sor);
                kutyak.add(k);
            }
            
            br.close();
            fajl.close();
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Hiba a beolvasáskor!");
        }
        btnBetoltes.setEnabled(false);
        insertTable();
        
        insertCombobox();
    }//GEN-LAST:event_btnBetoltesActionPerformed

    private void cbHanyKutyaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbHanyKutyaActionPerformed
        // TODO add your handling code here:
        
        
        int db = 0;
        for (KutyaClass hk : kutyak) {
            if (hk.getNem().equals(cbHanyKutya.getSelectedItem().toString())) {
                db++;
            }
        }
        
        txtHanyKutya.setText(String.valueOf(db));
    }//GEN-LAST:event_cbHanyKutyaActionPerformed

