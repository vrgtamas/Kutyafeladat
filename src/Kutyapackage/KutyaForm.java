    private void insertTable() {
        
        for (KutyaClass k : kutyak) {
            tblmodel.addRow(new Object[]{k.getNev(),k.getFajta()});
        }
    }

    private void insertCombobox() {
        
        ArrayList<String> nemek = new ArrayList<>();
        for (KutyaClass kn : kutyak) {
            if (!nemek.contains((kn.getNem()))) {
                nemek.add(kn.getNem());
            }
        }
        
        for (String szk : nemek) {
            cbHanyKutya.addItem(szk);
        }

    }
